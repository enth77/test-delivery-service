package deliveryservice.infra;

import deliveryservice.domain.*;
import org.springframework.hateoas.EntityModel;
import org.springframework.hateoas.Link;
import org.springframework.hateoas.server.RepresentationModelProcessor;
import org.springframework.stereotype.Component;

@Component
public class DeliveryJobHateoasProcessor
    implements RepresentationModelProcessor<EntityModel<DeliveryJob>> {

    @Override
    public EntityModel<DeliveryJob> process(EntityModel<DeliveryJob> model) {
        model.add(
            Link.of(model.getRequiredLink("self").getHref() + "/").withRel("")
        );
        model.add(
            Link.of(model.getRequiredLink("self").getHref() + "/").withRel("")
        );

        return model;
    }
}
